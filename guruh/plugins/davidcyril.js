const axios = require('axios');
const { addCmd } = require('../../guru/handlers/loader');
const { channelCtx } = require('../../guru/utils/gmdFunctions2');

const DC_BASE = 'https://apis.davidcyril.name.ng';
const TIMEOUT = 60_000;

async function dcJson(path) {
    const { data } = await axios.get(`${DC_BASE}${path}`, { timeout: TIMEOUT });
    return data;
}

async function dcBuffer(path) {
    const { data } = await axios.get(`${DC_BASE}${path}`, {
        timeout: TIMEOUT,
        responseType: 'arraybuffer',
    });
    return Buffer.from(data);
}

// ── DEEPSEEK V3 ──────────────────────────────────────────────
addCmd({
    name: 'deepseek',
    aliases: ['ds', 'ds3'],
    desc: 'Chat with DeepSeek-V3 AI',
    usage: 'deepseek <prompt>',
    category: 'ai',
    handler: async (ctx) => {
        const q = (ctx.args || []).join(' ').trim();
        if (!q) return ctx.reply('💬 Usage: `.deepseek <your prompt>`');
        await ctx.react('🧠').catch(() => {});
        try {
            const r = await dcJson(`/ai/deepseek-v3?text=${encodeURIComponent(q)}`);
            const ans = r?.response || r?.message || r?.result || '_(no answer)_';
            await ctx.reply(`🧠 *DeepSeek V3*\n\n${ans}`);
        } catch (e) {
            await ctx.reply(`❌ DeepSeek failed: ${e.message}`);
        }
    },
});

